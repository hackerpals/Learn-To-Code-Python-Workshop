#!/usr/bin/python
# Addition
print(1 + 3) # 4

# Subtraction
print(3 - 1) # 2

# Multiplication
print(3 * 2) # 6

# Division
print(6 / 2) # 3

# Modulus
print(7 % 2) # 1

# Absolute value
print(abs(2)) # 2
print(abs(-2)) # also 2

from math import sqrt, exp

print(sqrt(25)) # 5
print(exp(1)) # 2.718281828459045
